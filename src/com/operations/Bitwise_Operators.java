package com.operations;

public class Bitwise_Operators {
    public static void main(String[] args){
        //and operation

        int a = 14;
        int b = 6;
        System.out.println(a & b);

        //or operator
        System.out.println(a | b);

        //exclusively OR  (XOR)
        System.out.println(a^b);
    }
}
